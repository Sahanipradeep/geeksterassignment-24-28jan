package com.company;
import java.util.Scanner;
public class Main {
    /* by using for loop */
    public static void main(String[] args) {
        System.out.println("numbers are:");
        int i;
        for(i=10;i>=1;i--){
            if(i%2==0) {
                System.out.println(i);
            }
            }

    }

}

